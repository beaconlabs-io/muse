export interface GenerateRecipeHtmlOptions {
  recipe: Recipe;
  logicModelImageDataUrl?: string | null;
}

export function generateRecipeHtml({
  recipe,
  logicModelImageDataUrl,
}: GenerateRecipeHtmlOptions): string {
  const t = labels[recipe.locale];

  const grouped: Record<RecipeTargetCardType, Recipe["items"]> = {
    outputs: [],
    "outcomes-short": [],
    "outcomes-intermediate": [],
  };
  for (const item of recipe.items) {
    grouped[item.parentCardType].push(item);
  }

  const generatedAtDisplay = new Date(recipe.generatedAt).toLocaleString(
    recipe.locale === "ja" ? "ja-JP" : "en-US",
    { dateStyle: "long", timeStyle: "short" },
  );

  const safeLogicModelTitle = htmlEscape(recipe.logicModelTitle);

  const image = logicModelImageDataUrl
    ? `<img class="logic-model" src="${htmlEscape(logicModelImageDataUrl)}" alt="${htmlEscape(t.imageAlt)}">`
    : "";

  const sections = SECTION_ORDER.map((type) => renderSection(type, grouped[type], t)).join("");

  return `<!doctype html>
<html lang="${recipe.locale}">
<head>
<meta charset="utf-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>${htmlEscape(DOCUMENT_TITLE)}</title>
<style>${CSS}</style>
</head>
<body>
<div class="container">
<header class="cover">
<h1>${htmlEscape(DOCUMENT_TITLE)}</h1>
<p class="meta">${safeLogicModelTitle} · ${htmlEscape(generatedAtDisplay)} · ${htmlEscape(t.metricsCount(recipe.items.length))}</p>
${image}
</header>
<main>
${sections}
</main>
<footer class="app-footer">${htmlEscape(t.poweredBy)}</footer>
</div>
</body>
</html>`;
}

export function downloadRecipeHtml(html: string, filename: string): void {
  const blob = new Blob([html], { type: "text/html;charset=utf-8" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  setTimeout(() => URL.revokeObjectURL(url), 0);
}
