function sectionLabel(t: (typeof labels)[RecipeLocale], type: RecipeTargetCardType): string {
  switch (type) {
    case "outputs":
      return t.sectionOutputs;
    case "outcomes-short":
      return t.sectionShort;
    case "outcomes-intermediate":
      return t.sectionIntermediate;
  }
}

// Minimal HTML escape — covers the 5 characters that matter for attribute
// and text-content contexts. We never inject untrusted content into
// <script>/<style>/comment positions, so this is sufficient.
function htmlEscape(input: string): string {
  return input
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}

function renderList(items: string[]): string {
  if (items.length === 0) return "";
  return `<ul>${items.map((v) => `<li>${htmlEscape(v)}</li>`).join("")}</ul>`;
}

function renderOrderedList(items: string[]): string {
  if (items.length === 0) return "";
  return `<ol>${items.map((v) => `<li>${htmlEscape(v)}</li>`).join("")}</ol>`;
}

function renderMetricCard(item: Recipe["items"][number], t: (typeof labels)[RecipeLocale]): string {
  return `
    <article class="metric-card">
      <h3>${htmlEscape(item.metricName)}</h3>
      <p class="parent">${htmlEscape(t.parentLabel)}: ${htmlEscape(item.parentCardTitle)}</p>

      <section class="field">
        <h4>${htmlEscape(t.measurementSteps)}</h4>
        ${renderOrderedList(item.measurementSteps)}
      </section>

      <section class="field">
        <h4>${htmlEscape(t.dataCollectionMethod)}</h4>
        <p>${htmlEscape(item.dataCollectionMethod)}</p>
      </section>

      <div class="inline-row">
        <div class="inline-cell">
          <h4>${htmlEscape(t.frequency)}</h4>
          <p>${htmlEscape(item.frequency)}</p>
        </div>
        <div class="inline-cell">
          <h4>${htmlEscape(t.targetValue)}</h4>
          <p>${item.targetValue ? htmlEscape(item.targetValue) : t.none}</p>
        </div>
      </div>

      ${
        item.cautions.length > 0
          ? `<section class="field cautions"><h4>${htmlEscape(t.cautions)}</h4>${renderList(item.cautions)}</section>`
          : ""
      }
    </article>
  `;
}

function renderSection(
  type: RecipeTargetCardType,
  items: Recipe["items"],
  t: (typeof labels)[RecipeLocale],
): string {
  if (items.length === 0) return "";
  return `
    <section class="metric-section" data-type="${type}">
      <h2>${htmlEscape(sectionLabel(t, type))}</h2>
      ${items.map((i) => renderMetricCard(i, t)).join("")}
    </section>
  `;
}

