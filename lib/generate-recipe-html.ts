/**
 * Build a self-contained HTML document for a Recipe.
 *
 * "Self-contained" means: inline CSS, optional inline base64 image, no external
 * resources. The resulting string can be saved as `.html` and opened anywhere
 * — including offline or attached to email — and still render correctly.
 */

